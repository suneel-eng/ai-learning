# Arithmetic Operators
a = 10
b = 3
print(a + b)  # 13
print(a - b)  # 7
print(a * b)  # 30
print(a / b)  # 3.333...
print(a % b)  # 1 (modulo)
print(a ** b) # 1000 (exponentiation)


# Comparison Operators
print(2 < 3) # True
print(2 > 3) # False
print(2 == 3) # False
print(2 != 3) # True
print(2 <= 3) # True
print(2 >= 3) # False

# Logical Operators
print(True and True) # True
print(False and False) # False
print(True or False) # True
print(not(True)) # False

